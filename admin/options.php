  <h2>SnapApp</h2>
  <p>SnapApp is a platform that enables marketers and publishers to create quizzes, polls, surveys, sweepstakes, and contests, and embed them on their websites or Facebook pages. This Wordpress plugin provides an easy way to grab an app that you created on snapapp.com and insert it into a WordPress post or page. Here's now to use it:</p>
  <ol>
    <li>Create a SnapApp account at <a href="http://www.snapapp.com/register" target="_blank">http://www.snapapp.com/register</a> if you don't already have one.</li>
    <li>Create a new app: a trivia quiz, personality test, poll, survey, sweepstakes.</li>
    <li>Compose and style your app, and activate social sharing features.</li>
    <li>Publish your app. You will pay for your app at this time.</li>
    <li>Copy the app embed code.</li>
    <li>In Wordpress, click "Add New SnapApp" under the SnapApp plugin controls.</li>
    <li>Paste your app embed code into Wordpress.</li>
    <li>Insert your app into one or more blog posts.</li>
  </ol>
  <p>Note: If you want to add a SnapApp into your sidebar, footer or header, you do not need to use this plugin. You can add the app code directly into the code for those areas of your blog.</p>
